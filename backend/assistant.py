"""Threaded AI assistant — per-Module conversation, streamed responses, tool calls.

Threads are keyed by Module ID (the human-readable serial / barcode the lab
sticks on the panel — e.g. ``MOD-2026-001``). A single thread spans the full
test lifecycle for that module: Setup -> Live Monitor -> Data -> Analysis ->
Report across all 7 IEC test tabs. The assistant can pull run metadata, slice
telemetry, recompute Gate-2 analysis, and recommend a pass/fail verdict via
four explicit tool calls.

Storage
-------
The existing ``ai_thread`` table is reused without a migration: the string
Module ID is encoded into ``title`` as ``module:<id>`` so we can look threads
up with a single equality query. ``messages`` (JSON) holds the full
transcript as ``{role, content, ts, tool_calls?, tool_results?}`` entries.

Streaming
---------
``POST /api/assistant/threads/{module_id}/messages`` returns a
``text/event-stream`` (SSE). Events:

* ``meta``         — once at the start: thread id, model, demo flag.
* ``tool_call``    — name + args of a tool the assistant is invoking.
* ``tool_result``  — compact result payload from the tool.
* ``token``        — a chunk of the final assistant text.
* ``done``         — terminator with the persisted message id.
* ``error``        — bail-out reason; the user message is still persisted.

Tool routing
------------
Routing is deterministic and offline-friendly so tests pass without network
access:

* mention of "run", "session", "history" or a TR-ID  -> ``get_run``
* "telemetry", "samples", "last N readings"          -> ``query_telemetry``
* "analysis", "delta", "pmax", "gate"                -> ``recompute_analysis``
* "pass", "fail", "verdict", "recommend"             -> ``suggest_pass_fail``

If ``ANTHROPIC_API_KEY`` is set, the synthesized answer text is generated by
Claude with the same tool results pre-resolved and injected into the prompt;
otherwise a templated narrative is streamed. Either path produces the same
SSE event shape, so the client code is unchanged.
"""
