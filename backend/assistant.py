# --- Gate-2 threshold mirrors frontend/types/test-session.ts ---------------
GATE2_PMAX_DELTA_PERCENT = -5.0

# --- Title scheme used to key threads on Module ID ------------------------
THREAD_TITLE_PREFIX = "module:"

# Pattern to recognise TestRun ids spoken inline (e.g. "TR-42").
_RUN_ID_RE = re.compile(r"\b(?:TR|RUN|TR#|TR-)\s*[-:#]?\s*(\d{1,9})\b", re.IGNORECASE)


