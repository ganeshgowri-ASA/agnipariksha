class AssistantMessage(BaseModel):
    role: str = Field(pattern="^(user|assistant|system)$")
    content: str
    ts: float
    tool_calls: Optional[List[Dict[str, Any]]] = None
    tool_results: Optional[List[Dict[str, Any]]] = None


class ThreadOut(BaseModel):
    module_id: str
    thread_id: int
    created_at: datetime
    updated_at: datetime
    messages: List[AssistantMessage]


class SendMessageBody(BaseModel):
    message: str = Field(min_length=1, max_length=4000)
    # Optional context the UI may pass — current sub-tab, recent reading,
    # whatever helps the model. Never trusted as authoritative.
    context: Optional[Dict[str, Any]] = None
    # If the operator pinned a particular run to the chat — usually the run
    # currently visible in Live Monitor or Analysis sub-tab.
    test_run_id: Optional[int] = None


# ===========================================================================
# Thread store — thin wrapper around AIThread/messages JSON
# ===========================================================================
def _thread_title(module_id: str) -> str:
    return f"{THREAD_TITLE_PREFIX}{module_id}"


def _normalise_module_id(module_id: str) -> str:
    """Strip whitespace, enforce a length sane for a barcode/serial label.

    Empty values are rejected with HTTP 400 by the caller; we return the
    cleaned value so the title is stable across whitespace variations.
    """
    cleaned = (module_id or "").strip()
    if not cleaned or len(cleaned) > 128:
        raise HTTPException(status_code=400, detail="invalid module_id")
    return cleaned


def _ensure_messages_list(thread: AIThread) -> List[Dict[str, Any]]:
    """Coerce the JSON column into the canonical ``[{...}, ...]`` shape.

    Legacy rows stored ``{"m": [...]}`` (see ``test_db_models.py``); we
    normalise on read so callers see one shape only.
    """
    raw = thread.messages or {}
    if isinstance(raw, list):
        return list(raw)
    if isinstance(raw, dict):
        for key in ("messages", "items", "m"):
            v = raw.get(key)
            if isinstance(v, list):
                return list(v)
    return []


def get_or_create_thread(session: Session, module_id: str) -> AIThread:
    title = _thread_title(module_id)
    existing = session.exec(select(AIThread).where(AIThread.title == title)).first()
    if existing is not None:
        return existing
    thread = AIThread(title=title, messages={"messages": []})
    session.add(thread)
    session.commit()
    session.refresh(thread)
    return thread


def append_messages(session: Session, thread: AIThread, new_msgs: Iterable[Dict[str, Any]]) -> AIThread:
    msgs = _ensure_messages_list(thread)
    msgs.extend(new_msgs)
    thread.messages = {"messages": msgs}
    thread.updated_at = datetime.utcnow()
    session.add(thread)
    session.commit()
    session.refresh(thread)
    return thread


def clear_thread(session: Session, module_id: str) -> bool:
    title = _thread_title(module_id)
    existing = session.exec(select(AIThread).where(AIThread.title == title)).first()
    if existing is None:
        return False
    existing.messages = {"messages": []}
    existing.updated_at = datetime.utcnow()
    session.add(existing)
    session.commit()
    return True


