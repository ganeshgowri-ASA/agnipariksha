@router.get("/threads/{module_id}", response_model=ThreadOut)
def get_thread(module_id: str = Path(..., min_length=1, max_length=128)) -> ThreadOut:
    module_id = _normalise_module_id(module_id)
    with get_session() as session:
        thread = get_or_create_thread(session, module_id)
        msgs_raw = _ensure_messages_list(thread)
    msgs = [AssistantMessage(**m) for m in msgs_raw if isinstance(m, dict) and "role" in m]
    return ThreadOut(
        module_id=module_id,
        thread_id=thread.id or 0,
        created_at=thread.created_at,
        updated_at=thread.updated_at,
        messages=msgs,
    )


@router.delete("/threads/{module_id}", status_code=204)
def delete_thread(module_id: str = Path(..., min_length=1, max_length=128)) -> None:
    module_id = _normalise_module_id(module_id)
    with get_session() as session:
        if not clear_thread(session, module_id):
            raise HTTPException(status_code=404, detail="thread not found")


@router.post("/threads/{module_id}/messages")
async def post_message(
    body: SendMessageBody,
    module_id: str = Path(..., min_length=1, max_length=128),
) -> StreamingResponse:
    module_id = _normalise_module_id(module_id)

    async def _gen() -> AsyncIterator[str]:
        try:
            async for evt in stream_assistant_response(module_id, body):
                yield evt
        except Exception as exc:  # pragma: no cover - safety net for the SSE stream
            yield _sse("error", {"message": f"{type(exc).__name__}: {exc}"})

    return StreamingResponse(
        _gen(),
        media_type="text/event-stream",
        headers={
            "Cache-Control": "no-cache",
            "X-Accel-Buffering": "no",
            "Connection": "keep-alive",
        },
    )


